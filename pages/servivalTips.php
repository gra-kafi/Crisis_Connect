            <h2>Earthquake</h2>
            <ul>
                <li>Drop, cover, and hold on during shaking.</li>
                <li>Stay away from windows and heavy objects.</li>
                <li>After shaking stops, evacuate carefully if needed.</li>
            </ul>
        </section>

        <section class="disaster">
            <h2>Flood</h2>
            <ul>
                <li>Move to higher ground immediately.</li>
                <li>Avoid walking or driving through floodwaters.</li>
                <li>Listen to local alerts and evacuation orders.</li>
            </ul>
        </section>

        <section class="disaster">
            <h2>Cyclone</h2>
            <ul>
                <li>Secure loose objects and stay indoors.</li>
                <li>Keep emergency supplies ready.</li>
                <li>Evacuate if authorities advise to do so.</li>
            </ul>
        </section>

        <section class="disaster">
            <h2>Tsunami</h2>
            <ul>
                <li>Move to higher ground immediately.</li>
                <li>Do not return until officials declare it safe.</li>
                <li>Follow evacuation routes and signs.</li>
            </ul>
        </section>

        <section class="disaster">
            <h2>Volcanic Eruption</h2>
            <ul>
                <li>Stay indoors and close windows and doors.</li>
                <li>Wear masks to avoid inhaling ash.</li>
                <li>Evacuate if advised by authorities.</li>
            </ul>
        </section>
